// Sintaxe: === e !==. Verificam o tipo de dado, vão deixar o software mais confiavel garantindo que ira receber um número de fato com o valor numérico. Só será executado caso receba um número com um tipo de dado number;
// Essa expressão gera confiabilidade no código.

var numero = 5

if(numero === 5){
    console.log('O numero é 5')
}

if(numero !== 5){
    console.log('Não é um número 5 do tipo number')
}